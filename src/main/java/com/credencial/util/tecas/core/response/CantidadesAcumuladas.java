package com.credencial.util.tecas.core.response;

/**
 *
 * @author tecto
 */
public class CantidadesAcumuladas {

    private String interesesGenerados;
    private String interesesAcumuladosSobregiro;
    private String interesesAcumuladosSobregirosTecnicos;

    public String getInteresesGenerados() {
        return interesesGenerados;
    }

    public void setInteresesGenerados(String interesesGenerados) {
        this.interesesGenerados = interesesGenerados;
    }

    public String getInteresesAcumuladosSobregiro() {
        return interesesAcumuladosSobregiro;
    }

    public void setInteresesAcumuladosSobregiro(String interesesAcumuladosSobregiro) {
        this.interesesAcumuladosSobregiro = interesesAcumuladosSobregiro;
    }

    public String getInteresesAcumuladosSobregirosTecnicos() {
        return interesesAcumuladosSobregirosTecnicos;
    }

    public void setInteresesAcumuladosSobregirosTecnicos(String interesesAcumuladosSobregirosTecnicos) {
        this.interesesAcumuladosSobregirosTecnicos = interesesAcumuladosSobregirosTecnicos;
    }

}
