package com.credencial.util.request;

/**
 *
 * @author cj_sr
 */
public class Marca {

    private String exterior;
    private String atm;
    private String pos;
    private String ecomerce;
    private String telefonica;

    public String getExterior() {
        return exterior;
    }

    public void setExterior(String exterior) {
        this.exterior = exterior;
    }

    public String getAtm() {
        return atm;
    }

    public void setAtm(String atm) {
        this.atm = atm;
    }

    public String getPos() {
        return pos;
    }

    public void setPos(String pos) {
        this.pos = pos;
    }

    public String getEcomerce() {
        return ecomerce;
    }

    public void setEcomerce(String ecomerce) {
        this.ecomerce = ecomerce;
    }

    public String getTelefonica() {
        return telefonica;
    }

    public void setTelefonica(String telefonica) {
        this.telefonica = telefonica;
    }

}
